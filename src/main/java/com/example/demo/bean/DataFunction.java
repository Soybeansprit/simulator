package com.example.demo.bean;



public class DataFunction {
	public Double downTime=null;
	public Double upTime=null;
	public Double downValue=null;
	public Double upValue=null;
	public Function function=null;
	public Double getDownTime() {
		return downTime;
	}
	public void setDownTime(Double downTime) {
		this.downTime = downTime;
	}
	public Double getUpTime() {
		return upTime;
	}
	public void setUpTime(Double upTime) {
		this.upTime = upTime;
	}
	public Double getDownValue() {
		return downValue;
	}
	public void setDownValue(Double downValue) {
		this.downValue = downValue;
	}
	public Double getUpValue() {
		return upValue;
	}
	public void setUpValue(Double upValue) {
		this.upValue = upValue;
	}
	public Function getFunction() {
		return function;
	}
	public void setFunction(Function function) {
		this.function = function;
	}
}
